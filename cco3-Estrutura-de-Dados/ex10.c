//escreva um programa que coloque os números de 1 a 100 na tela na ordem inversa ( começando em 100 e terminado em 1)
//https://www.online-ide.com/PtCgkDvun4
#include <stdio.h>

int main(){
   int i;
   for(i = 100; i>=1; --i){  
       //i é igual a 100 e enquanto i for maior ou igual a 1 é decrementado 1 dele.
       printf("%d\n", i);
       
   }
}

